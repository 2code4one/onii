#ifndef ONII_FUZZY_HEDGE_VERY_HPP
#define ONII_FUZZY_HEDGE_VERY_HPP

/////////////////////////////////////////////////
/// @file onii/fuzzy/hedge/very.hpp
/////////////////////////////////////////////////

#include "abstract_hedge.hpp"

/////////////////////////////////////////////////
/// @namespace onii
/////////////////////////////////////////////////
namespace onii
{
/////////////////////////////////////////////////
/// @namespace onii::fuzzy
/////////////////////////////////////////////////
namespace fuzzy
{
/////////////////////////////////////////////////
/// @namespace onii::fuzzy::hedge
/////////////////////////////////////////////////
namespace hedge
{
/////////////////////////////////////////////////
/// @class very
/// @brief "very" fuzzy set hedge
/////////////////////////////////////////////////
class very :
    public abstract_hedge
{
public:

    /////////////////////////////////////////////////
    /// @brief Constructor
    ///
    /// @param[in] set - fuzzy set to hedge
    /////////////////////////////////////////////////
    very(abstract_set const &set) :
        abstract_hedge(set)
    {}

    /////////////////////////////////////////////////
    /// @brief Destructor
    /////////////////////////////////////////////////
    virtual ~very()
    {}

    /////////////////////////////////////////////////
    /// @brief Clone idiom
    ///
    /// @return A dynamic clone of the current instance
    /////////////////////////////////////////////////
    virtual abstract_set *clone() const
    {
        return new very(*this);
    }

    /////////////////////////////////////////////////
    /// @brief Get the degree of membership
    ///
    /// @param[in] crisp - crisp value
    /// @return The degree of membership of crisp
    /// @remarks The degree of membership is a value between 0 and 1.
    ///          The "very" hedge return the membership squared
    /////////////////////////////////////////////////
    virtual float membership(float crisp) const
    {
        float dom = abstract_hedge::membership(crisp);
        return dom * dom;
    }

}; // class very
} // namespace hedge
} // namespace fuzzy
} // namespace onii

#endif // ONII_FUZZY_HEDGE_VERY_HPP
