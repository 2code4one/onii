#ifndef ONII_FUZZY_HEDGE_FAIRLY_HPP
#define ONII_FUZZY_HEDGE_FAIRLY_HPP

/////////////////////////////////////////////////
/// @file onii/fuzzy/hedge/fairly.hpp
/////////////////////////////////////////////////

#include <cmath>
#include "abstract_hedge.hpp"

/////////////////////////////////////////////////
/// @namespace onii
/////////////////////////////////////////////////
namespace onii
{
/////////////////////////////////////////////////
/// @namespace onii::fuzzy
/////////////////////////////////////////////////
namespace fuzzy
{
/////////////////////////////////////////////////
/// @namespace onii::fuzzy::hedge
/////////////////////////////////////////////////
namespace hedge
{
/////////////////////////////////////////////////
/// @class fairly
/// @brief "fairly" fuzzy set hedge
/////////////////////////////////////////////////
class fairly :
    public abstract_hedge
{
public:

    /////////////////////////////////////////////////
    /// @brief Constructor
    ///
    /// @param[in] set - fuzzy set to hedge
    /////////////////////////////////////////////////
    fairly(abstract_set const &set) :
        abstract_hedge(set)
    {}

    /////////////////////////////////////////////////
    /// @brief Destructor
    /////////////////////////////////////////////////
    virtual ~fairly()
    {}

    /////////////////////////////////////////////////
    /// @brief Clone idiom
    ///
    /// @return A dynamic clone of the current instance
    /////////////////////////////////////////////////
    virtual abstract_set *clone() const
    {
        return new fairly(*this);
    }

    /////////////////////////////////////////////////
    /// @brief Get the degree of membership
    ///
    /// @param[in] crisp - crisp value
    /// @return The degree of membership of crisp
    /// @remarks The degree of membership is a value between 0 and 1.
    ///          The "fairly" hedge return the membership square root
    /////////////////////////////////////////////////
    virtual float membership(float crisp) const
    {
        return std::sqrt(abstract_hedge::membership(crisp));
    }

}; // class fairly
} // namespace hedge
} // namespace fuzzy
} // namespace onii

#endif // ONII_FUZZY_HEDGE_FAIRLY_HPP
